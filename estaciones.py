

mes=input("Digite el mes:")

if(mes == "enero" or mes == "febrero" or mes == "marzo"):
    print(f'la estacion del mes es invierno')

elif(mes == "abril" or mes == "marzo" or mes == "junio"):
    print(f'la estacion del mes es primavera')

elif(mes == "julio" or mes == "agosto" or mes == "septiembre"):
    print(f'la estacion del mes es verano')

elif(mes == "octubre" or mes == "noviembre" or mes == "diciembre"):
    print(f'la estacion del mes es otoño')

else:
    print(f'Debe ingresar un mes del año valido')    






















